import 'package:flutter/material.dart';

class IconPicker extends StatefulWidget {
  const IconPicker({
    super.key,
    this.initialIcon,
    required this.color,
    required this.onIconChangedCallback,
  });

  final IconData? initialIcon;
  final Color color;
  final Function(IconData) onIconChangedCallback;

  @override
  State<IconPicker> createState() => _IconPickerState();
}

class _IconPickerState extends State<IconPicker> {
  IconData? _currentIcon;
  String? _searchString;

  @override
  Widget build(BuildContext context) {
    _currentIcon = widget.initialIcon ?? Icons.blur_on;
    return Padding(
      padding: const EdgeInsets.symmetric(horizontal: 8),
      child: InkWell(
        onTap: () {
          _searchString = null;
          showDialog(
            context: context,
            builder: (BuildContext context) {
              return StatefulBuilder(
                builder: (context, setState) {
                  return SimpleDialog(
                    shape: const RoundedRectangleBorder(
                      borderRadius: BorderRadius.all(
                        Radius.circular(8),
                      ),
                    ),
                    contentPadding: const EdgeInsets.only(top: 0),
                    children: [
                      TextField(
                        decoration: InputDecoration(
                          hintText: 'Search',
                          prefixIcon: Icon(
                            Icons.search,
                            color: Theme.of(context).colorScheme.tertiary,
                          ),
                          hintStyle: TextStyle(
                              color: Theme.of(context).colorScheme.tertiary),
                        ),
                        onChanged: (String value) {
                          setState(() {
                            _searchString = value;
                          });
                        },
                      ),
                      getIcons(),
                    ],
                  );
                },
              );
            },
          );
        },
        child: Icon(
          size: 48,
          _currentIcon,
          color: widget.color,
        ),
      ),
    );
  }

  Widget getIcons() {
    List<IconData> filteredIcons = [];
    for (Map<String, dynamic> element in _fullIconList) {
      // ignore: avoid_dynamic_calls
      if (element['name'].contains(
          _searchString != null ? _searchString!.toLowerCase() : '')) {
        filteredIcons.add(element['icon']);
      }
    }
    return SizedBox(
      height: 500,
      width: double.maxFinite,
      child: GridView.count(
        crossAxisCount: 5,
        children: List.generate(filteredIcons.length, (index) {
          return InkWell(
            onTap: () {
              setState(() {
                _currentIcon = filteredIcons[index];
                widget.onIconChangedCallback(filteredIcons[index]);
                Navigator.pop(context);
              });
            },
            child: Icon(
              filteredIcons[index],
              color: widget.color,
            ),
          );
        }),
      ),
    );
  }

